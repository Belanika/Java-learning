package basics;

public class DataTypes {
    public static void main(String[] args) {
        //Data type
        byte num1 = 120;
        short num2 = 10000;
        int num3 = 457486835;
        long num4 = 4856074835L;

        //Floating point data type
        float num5 = 1454.3467f;
        double num6 = 454879.4485756435d;

        //Boolean data type
        boolean lightSwitch = true;

        //Character data type
        char char1 = '%';
        char char2 = 'y';
        char char3 = '0';
        char char4 = '\n';

        //String data type
        String text1 = "This is a very good text";


        //Print out variable values
        System.out.println(num1);
        System.out.println(num6);
        System.out.println(lightSwitch);
        System.out.println(char3);
    }
}
